function gp(){
    var x = document.getElementById("gp");
    var y = document.getElementById("x1");
    if(x.style.display==="none"){
        x.style.display="";
        y.innerHTML="&downarrow;"
    }
    else{
        x.style.display="none";
        y.innerHTML="&uparrow;"
    }
}

